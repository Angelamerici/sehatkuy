				<h2>MEMELIHARA GINJAL TETAP SEHAT  </h2>
			<img src="<?php echo site_url('images/post4.jpg'); ?>" alt="post4" style="width:800px;height:400px">
			
				<p>Dalam kesehatan ada adagium, kesehatan bukanlah segalanya, namun segalanya tanpa kesehatan bukanlah apa-apa. 
				Health is not everything, but everything without health is nothing.Kiranya adagium di atas sangat tepat, bila 
				dikaitkan dengan paradigma sehat. Yakni bahwa mempertahankan tetap sehat melalui perilaku hidup sehat lebih penting 
				dan bermakna dari pada mengobati setelah jatuh sakit.</p>
				<p>Ginjal, sebagai organ tubuh untuk ekskresi, berfungsi untuk membuang sampah metabolisme, menjaga keseimbangan cairan 
				tubuh dan elektrolit, serta membuat hormon untuk pengaturan tekanan darah dan pembentukan sel darah merah. Agar tubuh 
				tetap berfungsi dengan optimal, orang dengan gagal ginjal harus menjalani hemodialisis sebagai substitusi dari ginjal yang 
				telah rusak.</p>
				
				<h4>Memelihara kesehatan ginjal</h4>
				
				
				<p>Ginjal sebagai organ vital harus dipelihara agar tetap sehat. Berikut adalah beberapa tips untuk memelihara kesehatan 
				ginjal. Pertama, cukupi kebutuhan cairan tubuh dengan minum air. Secara umum kebutuhan air adalah sekitar 2 liter. 
				Apabila diasumsikan buah, sayur, sup, dan lain-lain, menyumbangkan 500 ml air, maka kebutuhan minum air adalah sekitar 1,5 
				liter. Tentu, kebutuhan ini bersifat relatif tergantung aktivitas seseorang. Bila banyak berkeringat tentu kebutuhan air 
				meningkat.</p>
				
				<p>Kedua, mengonsumsi makanan yang sehat. Makan makanan yang sehat, misal dengan memperbanyak buah dan sayur, akan 
				memelihara kesehatan ginjal. Hindari makan terlalu banyak lemak dan makanan yang mengandung purin (jerohan). Lemak tidak 
				jenuh akan menimbulkan penebalan pembuluh darah, yang ujungnya menyebabkan hipertensi. Makanan banyak mengandung purin akan 
				meningkatkan asam urat, yang bisa mengganggu fungsi ginjal.</p>
				
				<p>Ketiga, lakukan olah raga teratur. Olah raga teratur akan mempertahankan berat badan ideal dan menurunkan tekanan darah. 
				Dengan berat badan ideal dan tensi yang normal akan menyehatkan ginjal.</p>
				<p>Keempat, hati-hati dengan suplemen dan jamu yang mengandung bahan kimia obat. Beberapa suplemen mengandung asam amino yang tinggi. Asam amino yang tinggi tentu akan membebani 
				ginjal. Konsumsilah suplemen sesuai dengan aturan pakai yang tertera pada kemasan. Jamu yang dicampuri dengan bahan kimia obat 
				juga berbahaya untuk kesehatan ginjal. Bahan kimia obat yang dicampurkan sering tidak jelas dosisnya. Dan, tindakan semacam ini 
				melanggar aturan Badan POM.</p>

				<p>Kelima, berhentilah merokok. Zat-zat toksik dalam rokok dapat merusak pembuluh darah, yang ujungnya dapat menyebabkan hipertensi. 
				Naiknya tekanan darah dapat menyebabkan terganggunya fungsi ginjal. Itulah sebabnya, hipertensi merupakan faktor risiko terjadinya PGK.</p>

				<p>Ketujuh, jika menderita hipertensi atau kencing manis, maka harus dipantau fungsi ginjalnya secara berkala. Biasanya dokter yang 
				merawat akan memantau fungsi ginjal (Renal Function Test) secara berkala. Perlu diketahui bahwa salah satu komplikasi kencing manis 
				adalah diabetic nephropathy (kerusakan ginjal akibat diabetes).</p>
				
				
				<p>Masyarakat luas harus mendapatkan pemahaman yang utuh dan holistik terkait dengan berbagai upaya pencegahan untuk mempertahankan 
				ginjal tetap sehat. Adagium bahwa mencegah lebih baik dari pada mengobati adalah sangat tepat untuk mempertahankan kesehatan ginjal. 
				Teknologi kedokteran memang selalu berkembang dalam memberikan solusi pengobatan. Namun, harus diingat bahwa teknologi sering kali 
				beriringan dengan mahalnya biaya yang harus dikeluarkan. Dalam hal ini termasuk teknologi hemodialisis (cuci darah). Semoga masyarakat 
				semakin sadar tentang pentingnya menjaga kesehatan ginjal </p>
				
