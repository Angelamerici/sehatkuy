				<h2>7 Tips Pola Hidup Sehat yang Harus Mulai Dilakukan dari Sekarang </h2>
			<img src="<?php echo site_url('images/post3.jpg'); ?>" alt="post3" style="width:800px;height:400px">
			
				<p>Sebenarnya menjaga tubuh agar tetap sehat itu bukan hal yang sulit loh sobat, 
				bahkan tidak perlu mengahabiskan banyak biaya. Hal yang terpenting adalah selalu 
				konsisten dan dispilin dalam menjalani pola hidup sehat.</p>
				<p>Menjalankan pola hidup sehat bagi beberapa orang memang bukan sesuatu hal yang 
				mudah dilakukan, tetapi perlu diingat bahwa pola hidup sehat mempunyai banyak manfaat,
				seperti membantu menjaga berat badan, meningkatkan energi, sampai mencegah timbulnya penyakit.</p>
				<p>Nah, bagi kalian yang sudah siap untuk memulai pola hidup sehat, yuk simak beberapa tips dibawah ini:</p>
				
				<ol>
				<li><p>Olahraga secara rutin</p></li>
				<br><br>
				<p>Olahraga dengan cara yang benar dan waktu yang tepat dapat mebawa manfaat yang luar biasa bagi 
				kesehatan tubuh, termasuk menjaga berat badan ideal, meningkatkan kepadatan tulang, serta menurukan 
				resiko berbagai macam penyakit.</p>
				<p>Bagi kalian yang jarang berolahraga dan ingin memulainya, mulailah dengan olahraga yang ringan 
				terlebih dulu, seperti senam, jalan santai, jogging, dll. Berolahraga sebaiknya dilakukan secara 
				teratur dengan jadwal yang ditentukan sendiri, sebaiknya olahraga dilakukan sebanyak 3-5 kali seminggu 
				dengan durasi minimal 30 menit setiap harinya.</p>
				
				<li><p>Makan secara sehat dan seimbang</p></li>
				<br><br>
				<p>Agar tubuh dapat tumbuh dan berkembang serta menjalankan fungsinya dengan maksimal, maka dibutuhkan 
				asupan gizi yang seimbang. Cara memasaknya pun sebisa mungkin tidak merusak zat gizi dan serat sehingga 
				masih memberi manfaat bagi yang mengonsumsinya.</p>
				<p>Makan dalam porsi yang sedikit tetapi sering lebih dianjurkan daripada makan dalam porsi yang besar 
				sekaligus. Dengan pola makan seperti ini, metabolisme tubuh dapat bekerja lebih lancar dan berat badan 
				tubuh juga akan terjaga.Kurangi makanan olahan dan makanan kaleng serta konsumsi daging, hal ini dikarenakan 
				berdasar dari penelitian yang dilakukan oleh <italic>Harvard T.H Chan School of Public Health</italic>, 
				para ahli memaparkan bahwa kandungan zat besi pada daging merah dapat menghambat kerja insulin dan merusak sel pankreas.</p>
				<p>Disarankan untuk mengonsumsi beragam buah dan sayuran setidaknya sembilan porsi dalam sehari sebagai bagian 
				dari pola hidup sehat.</p>
				
				<li><p>Minum air putih secara teratur</p></li>
				<br><br>
				<p>Sebanyak 80% dari tubuh manusia adalah berupa cairan. Oleh karena itu, sangat disarankan untuk memperbanyak 
				konsumsi air mineral untuk menjaga asupan cairan tubuh dan mencegah terjadinya dehidrasi.</p>
				<p>Meminum air putih setiap harinya akan membantu fungsi metabolisme tubuh terjaga, sehingga akan membantu untuk 
				memaksimalkan kerja setiap organ tubuh untuk menyingkirkan berbagai racun di dalam tubuh. Setidaknya kita harus 
				meminum air sebanyak minimal 8 gelas setiap hari.</p>
		
				<li><p>Konsumsi buah dan sayur serta kacang-kacangan</p></li>
				<br><br>
				<p>Bebearapa kandungan yang ada di dalam buah dan sayur mampu mencegah terjadinya pertumbuhan sel kanker seperti 
				antioksidan yang bisa mengikat radikal bebas yang dapat merusak tubuh. Buah-buahan dan sayuran juga kaya akan 
				kandungan vitamin yang diperlukan oleh tubuh.</p>
				<p>Selain itu, segala jenis kacang-kacangan seperti almond, kenari kacang tanah, dan lainnya mengandung asam lemak dengan omega 3.</p>
				
				<li><p>Hidup bersih</p></li>
				<br><br>
				<p>Selalu menjaga kebersihan diri dan lingkungan sekitar agar selalu bersih juga menjadi pola hidp sehat yang wajib diterapkan. 
				Hal ini dikarenakan bakteri atau virus penyebab penyakit akan berkembang biak di tempat yang kotor.</p>
				
				<li><p>Istirahat cukup</p></li>
				<br><br>
				<p>Istirahat cukup adalah kunci mendapatkan tubuh dan pikiran yang sehat. Menurut pakar kesehatan tidur ideal orang dewasa 
				minimal adalah 8-9 jam per hari. </p>
				<p>Selain tidur dengan waktu yang pas, pastikan jam tidur terpenuhi dengan membiasakan tidur dan bangun di waktu yang sama 
				setiap harinya, hal ini dilakukan agar tubuh dapat lebih siap kapan harus istirahat dan kapan harus beraktivitas.</p>
				
				<li><p>Bersyukur</p></li>
				<br><br>
				<p>Banyak bersyukur juga merupakan cara untuk menjaga kesehatan tubuh dan pikiran. Dengan bersyukur, kita dapat menemukan sisi 
				positif dari setiap kejadian yang kita alami dan terhindar dari stress.</p>
				<p>Rasa sykur yang mengakar dan terwujud dalam kehidupan sehari-hari akan membuat pikiran dan hati yang tenang. Sehat secara 
				rohani juga akan memengaruhi kesehatan secara fisik.</p>
				</ol>
				<br>
				<p>Nah, itulah beberapa tips-tips pola hidup sehat dari kami yang harus mulai dilakukan dari sekarang. Pada dasarnya hidup sehat mudah untuk dilakukan. Kuncinya, selalu disiplin dan konsisten dalam menjalani pola 
				hidup sehat seperti yang diuraikan diatas. Jika kita bisa bisa konsisten dan disiplin maka kesehatan akan selalu terjaga.</p>
				
