				<h2>Kebiasaan Hidup Sehat yang Baik Dilakukan Sebelum Tidur Malam </h2>
			<img src="<?php echo site_url('images/post2.jpg'); ?>" alt="post2" style="width:800px;height:400px">
			
				<p>Kebiasaan hidup sehat memang bisa dilakukan di mana pun dan kapanpun, bisa dilakukan di sela-sela 
				waktu luang. Tapi tahukah kamu, ada beberapa kebiasaan yang bisa memberikan manfaat yang maksimal jika 
				dilakukan saat malam hari sebelum tidur. Mengapa bisa demikian?</p>
				<p>Hal ini dikarenakan, tubuh memproduksi sel-sel lebih optimal di malam hari. Jika kita melakukan pola 
				hidup sehat di malam hari, maka hal ini dapat membantu memaksimalkan proses regenerasi sel atau pergantian 
				sel-sel mati dengan sel-sel baru sehingga sel tubuh kita akan semakin sehat.</p>
				<p>Berikut merupakan beberapa kebiasaan hidup sehat yang baik dilakukan sebelum tidur malam yang dapat kami uraikan:</p>
				
				<ol>
				<li><p>Olahraga</p></li>
				<br><br>
				<p>Olahraga pagi maupun olahraga malam sebenarnya sama-sama memiliki dampak yang baik bagi kesehatan. Hanya saja, 
				olahraga pada malam hari sebelum tidur memberikan manfaat berlebih untuk kesehatan tubuh.</p>
				<p>Saat kita tidur, pernapasan cenderung lebih pendek, yang berarti jumlah oksigen yang kita hirup menjadi lebih sedikit. 
				Nah, untuk mendapat pasokan oksigen yang lebih banyak kita perlu melakukan olahraga di malam hari.</p>
				<p>Cukup meluangkan waku sekitar 15-20 menit untuk melakukan olahraga ringan sebelum tidur, seperti berjalan kaki atau 
				latihan pernapasan. Dengan begini, dapat membantu mengalirkan oksigen ke dalam sel-sel tubuh dan menjaganya tetap sehat.</p>
				
				<li><p>Mencuci muka</p></li>
				<br><br>
				<p>Rutin mencuci muka di malam hari sebelum tidur dapat memaksimalkan proses peremaajaan kulit secara alami. Selain itu 
				juga dapat membuat kulit wajah menjadi bersih, sehingga kulit dan wajah akan terbebas dari bakteri penyebab jerawat dan membuat wajah tampak awet muda.</p>
				
				<li><p>Makan yoghurt</p></li>
				<br><br>
				<p>SMenurut Irina Zhdanova, MD.PhD, seorang ahli tidur dan CEO ClockCoach, makan yogurt sebelum tidur dapat membantu melancarkan sistem pencernaan sekaligus 
				menjaga kekebalan tubuh kita di sepanjang malam. Selain itu, kandungan protein pada yogurt dapat mengoptimalkan perbaikan otot tubuh yang rusak setelah beraktivitas seharian.</p>
		
				<li><p>Pakai lotion</p></li>
				<br><br>
				<p>Body lotion atau tabir surya biasanya digunakan kebanyakan orang pada pagi hari untuk melindungi kulit dari paparan sinar matahari. Selain itu, body lotion juga mengandung 
				retinoid yang dapat mendorong pembentukan kolagen pada kulit, yang dapat membuat kulit menjadi kenyal, lembap, dan sehat jika digunakan secara rutin.</p>
				<p>Namun, banyak yang belum mengetahui bahwa kandungan retinoid dalam body lotion tersebut bisa rusak jika terpapar sinar UV matahari. Maka, sebaiknya body lotion yang mengandung 
				retinoid digunakan juga pada malam hari sebelum tidur, sehingga kulit dapat menyerap manfaat retinoid secara maksimal.</p>
				
